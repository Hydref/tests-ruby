def add(first_param, second_param)
	return (first_param + second_param)
end

def subtract(first_param, second_param)
	return (first_param - second_param)
end

def sum(array)
	return array.sum
end

def multiply(first_var, second_var)
	return first_var * second_var
end

def power(first_var, second_var)
	return first_var ** second_var
end